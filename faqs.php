  <h1>Data Privacy Policy</h1>
<p>
  <strong>Kolehiyo ng Lungsod ng Dasmariñas (KLD)</strong> is fully committed to protecting the confidentiality of personal data collected through the 
  <strong>QR Violation Recorder</strong>, a student-developed project designed to monitor student violations using QR code technology. 
  We respect your privacy and ensure that all personal information is handled in accordance with the Data Privacy Act of 2012 (RA 10173) and guidelines from the National Privacy Commission (NPC).
</p>

<h2>Data Collection</h2>
<p>
  The QR Violation Recorder may collect personal data such as:
  <br>• Student name, ID number, course, and year level
  <br>• Logged violation history linked to the student’s unique QR code
  <br>• Technical information such as device type, browser, and scan logs
  <br>• Any additional information voluntarily provided by students or authorized personnel
</p>
<p>
  The system may also use session data or cookies to improve functionality and facilitate faster QR scanning. You may request to disable these features, but some functionality may be affected.
</p>

<h2>Data Usage</h2>
<p>
  Personal data collected through the QR Violation Recorder is used strictly for school-related purposes:
  <br>• Logging and monitoring student violations
  <br>• Reviewing and clearing violation records during clearance events
  <br>• Generating administrative reports and supporting school research or statistical analysis
  <br>All processing is done to fulfill legitimate educational and administrative purposes, and only authorized personnel may access the data.
</p>

<h2>Data Sharing and Disclosure</h2>
<p>
  All data collected is treated as confidential and will not be shared with unauthorized parties. Only designated school staff or project team members may access the system. 
  Data may be shared only:
  <br>• With the consent of the student
  <br>• When required by law or school policy
</p>
<p>
  The system does not sell or distribute personal data for commercial purposes.
</p>

<h2>Data Retention</h2>
<p>
  Student data and violation records are retained only as long as necessary for documentation, reporting, and clearance purposes. Once the information is no longer required, it will be securely deleted or anonymized.
</p>

<h2>Data Security</h2>
<p>
  Reasonable measures are implemented to protect personal data from unauthorized access, alteration, or loss. Access to the system is limited to authorized users, and all logs and records are stored securely.
</p>

<h2>Data Subject’s Rights</h2>
<p>
  Students have the right to:
  <br>• Access their personal data and violation records
  <br>• Request corrections if information is inaccurate
  <br>• Raise concerns about data processing through authorized faculty or project team members
</p>

<h2>Changes in the Privacy Notice</h2>
<p>
  This privacy notice applies to the QR Violation Recorder system. We reserve the right to update or modify this policy as needed. Any changes will be reflected in the system and communicated to relevant users if necessary.
</p>

<h2>Contact Us</h2>
<p>
  For questions or concerns regarding this privacy notice or the handling of personal data, please contact the designated data protection officer at <strong>qrviolationrecorder@email.com</strong>.
</p>


