      <h2>FAQs (Frequently Asked Questions)</h2>

      <div class="faq-item">
        <button class="faq-question">1. What is QR Violation Recorder?</button>
        <div class="faq-answer">QR Violation Recorder is a student-created system that uses QR codes to help track and manage student violations at Kolehiyo ng Lungsod ng Dasmariñas.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">2. Is QR Violation Recorder an official school system?</button>
        <div class="faq-answer">No. QR Violation Recorder is a student project developed as part of an academic requirement. It is not officially operated by the school administration.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">3. Who scans my QR code?</button>
        <div class="faq-answer">The school guard scans it during a violation incident, and authorized faculty scan it during clearance day to check your records.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">4. Is my data safe?</button>
        <div class="faq-answer">Yes. Your data is stored securely and is only accessed by authorized personnel for clearance and record-checking purposes.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">5. Can I see my violations?</button>
        <div class="faq-answer">Yes. You can view your record through the system interface or by inquiring from the project team or Student Affairs Office.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">6. What if I have no violations?</button>
        <div class="faq-answer">When scanned during clearance, your QR will simply show no records, making the process faster.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">7. Can my QR code be replaced if lost?</button>
        <div class="faq-answer">Yes. You may request a replacement from the project team or the designated faculty in charge of QR Violation Recorder.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">8. Can parents view the violation records?</button>
        <div class="faq-answer">Parents may inquire through proper school channels or authorized faculty with the student's consent.</div>
      </div>

      <div class="faq-item">
        <button class="faq-question">9. What devices can scan the QR code?</button>
        <div class="faq-answer">Only devices with the QR Violation Recorder scanning feature or authorized personnel tools can scan and access the data.</div>
      </div>

